def classify_file(filename):
    """根据文件名匹配分类规则，返回 (大类, 子类)"""
    for category, subcategory, pattern in RULES:
        if re.search(pattern, filename):
            return category, subcategory
    return "未分类", "未分类"


def main():
    script_dir = os.path.dirname(os.path.abspath(__file__))
    input_csv = os.path.join(script_dir, "file_list.csv")
    output_csv = os.path.join(script_dir, "file_classified.csv")

    if not os.path.exists(input_csv):
        print(f"错误: 找不到 {input_csv}")
        sys.exit(1)

    # 读取文件列表
    rows = []
    with open(input_csv, "r", encoding="utf-8-sig") as f:
        reader = csv.DictReader(f)
        for row in reader:
            rows.append(row)

    print(f"读取文件数: {len(rows)}")

    # 分类
    category_counter = Counter()
    subcategory_counter = Counter()
    classified_rows = []

    for row in rows:
        filename = row.get("文件名", "")
        cat, subcat = classify_file(filename)
        category_counter[cat] += 1
        subcategory_counter[f"{cat} > {subcat}"] += 1
        classified_rows.append({
            **row,
            "大类": cat,
            "子类": subcat,
        })

    # 输出统计
    print("\n" + "=" * 70)
    print("大类统计")
    print("=" * 70)
    for cat, cnt in category_counter.most_common():
        pct = cnt / len(rows) * 100
        bar = "█" * int(pct / 2)
        print(f"  {cat:<20s} {cnt:>5d} ({pct:5.1f}%) {bar}")

    print("\n" + "=" * 70)
    print("子类统计")
    print("=" * 70)
    for subcat, cnt in subcategory_counter.most_common():
        print(f"  {subcat:<45s} {cnt:>5d}")

    # 未分类文件样本
    unclassified = [r["文件名"] for r in classified_rows if r["大类"] == "未分类"]
    if unclassified:
        print(f"\n未分类文件 ({len(unclassified)} 个)，样本:")
        for name in unclassified[:30]:
            print(f"  - {name}")

    # 写出分类结果
    fieldnames = list(rows[0].keys()) + ["大类", "子类"]
    with open(output_csv, "w", newline="", encoding="utf-8-sig") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(classified_rows)

    print(f"\n分类结果已导出: {output_csv}")


if __name__ == "__main__":
    main()
