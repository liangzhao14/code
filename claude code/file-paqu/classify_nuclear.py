def classify_file(filename):
    """根据文件名匹配分类规则，返回 (一级, 二级, 三级)"""
    for level1, level2, level3, pattern in RULES:
        if re.search(pattern, filename):
            return level1, level2, level3
    return "待清洗", "未分类", "未分类"


def main():
    script_dir = os.path.dirname(os.path.abspath(__file__))
    input_csv = os.path.join(script_dir, "file_list.csv")
    output_csv = os.path.join(script_dir, "file_classified_nuclear.csv")

    if not os.path.exists(input_csv):
        print(f"错误: 找不到 {input_csv}", file=sys.stderr)
        sys.exit(1)

    rows = []
    with open(input_csv, "r", encoding="utf-8-sig") as f:
        reader = csv.DictReader(f)
        for row in reader:
            rows.append(row)

    # 排除非数据文件
    rows = [r for r in rows if not r["文件名"].endswith(('.csv', '.py'))]

    print(f"读取文件数: {len(rows)}")

    l1_counter = Counter()
    l2_counter = Counter()
    l3_counter = Counter()
    classified_rows = []

    for row in rows:
        filename = row.get("文件名", "")
        c1, c2, c3 = classify_file(filename)
        l1_counter[c1] += 1
        l2_counter[f"{c1} > {c2}"] += 1
        l3_counter[f"{c1} > {c2} > {c3}"] += 1
        classified_rows.append({
            **row,
            "一级分类": c1,
            "二级分类": c2,
            "三级分类": c3,
        })

    # 输出统计
    print(f"\n{'='*70}")
    print("一级分类")
    print(f"{'='*70}")
    for cat, cnt in l1_counter.most_common():
        pct = cnt / len(rows) * 100
        print(f"  {cat:<12s} {cnt:>5d} ({pct:5.1f}%)")

    print(f"\n{'='*70}")
    print("二级分类")
    print(f"{'='*70}")
    for cat, cnt in l2_counter.most_common():
        print(f"  {cat:<40s} {cnt:>5d}")

    print(f"\n{'='*70}")
    print("三级分类")
    print(f"{'='*70}")
    # 按一级分组输出
    for l1 in ["法规", "标准", "专业知识", "企业私域", "待清洗"]:
        subset = {k: v for k, v in l3_counter.items() if k.startswith(l1)}
        if not subset:
            continue
        print(f"\n  [{l1}]")
        for cat, cnt in sorted(subset.items(), key=lambda x: -x[1]):
            label = cat.replace(f"{l1} > ", "  ")
            print(f"    {label:<50s} {cnt:>4d}")

    # 未分类样本
    unclassified = [r["文件名"] for r in classified_rows
                    if r["一级分类"] == "待清洗" and r["二级分类"] == "未分类"]
    if unclassified:
        print(f"\n{'='*70}")
        print(f"仍未分类: {len(unclassified)} 个")
        print(f"{'='*70}")
        for name in sorted(unclassified):
            print(f"  {name}")

    # 写出
    fieldnames = list(rows[0].keys()) + ["一级分类", "二级分类", "三级分类"]
    with open(output_csv, "w", newline="", encoding="utf-8-sig") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(classified_rows)

    print(f"\n分类结果已导出: {output_csv}")


if __name__ == "__main__":
    main()
